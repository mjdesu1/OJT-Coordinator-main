// this function checks if the file input is empty or not
function Cfile1() {
  var fileInput = document.getElementById("File1");
  var submitButton = document.getElementById("SBfile1");

  if (fileInput.value === "") {
    submitButton.disabled = true;
  } else {
    submitButton.disabled = false;
  }
}

